TEST(JournalReadState, TestRoundTrip)
{
    std::string_view data(reinterpret_cast<const char*>(
                              test_redfish_core_include_journal_2_bin.data()),
                          test_redfish_core_include_journal_2_bin.size());
    DuplicatableFileHandle temporaryFile(data);

    JournalReadState state = JournalReadState::openFile(temporaryFile.filePath);
    state.next();

    // Note, this is technically opaque, so if it changes in the future
    EXPECT_EQ(state.getCursor(),
              "s=7aaf76c228424b688f271f41c4f8ab19;"
              "i=d040000;"
              "b=eaa12e110a5f45b7b16192c8eef5465e;"
              "m=13e2051bdb6;t=64aa890c35520;"
              "x=c044c3c32c68c66f");
    EXPECT_EQ(state.getRealtimeUsec(), 1770937703945504);
}
